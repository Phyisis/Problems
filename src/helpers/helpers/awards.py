def lucky(n):
    L = list(range(1, n + 1, 2))
    j = 1
    Lj = 2
    while j <= len(L) - 1 and L[j] <= len(L):
        L = [Li for i, Li in enumerate(L) if (i + 1) % L[j]]
        j += 1
    return L

def triangle(n):
    L = []
    i = 1
    while True:
        t = (i*(i+1))//2
        if t < n:
            L.append(t)
            i += 1
            continue
        break
    return L

